void CWeaponGlock18C::DryFire(void)
{
	//WeaponSound(EMPTY);
	SendWeaponAnim(ACT_VM_DRYFIRE);

	m_flSoonestPrimaryAttack = gpGlobals->curtime + PISTOL_FASTEST_DRY_REFIRE_TIME;
	m_flNextPrimaryAttack = gpGlobals->curtime + SequenceDuration();
}

//-----------------------------------------------------------------------------
// Purpose:
//-----------------------------------------------------------------------------
void CWeaponGlock18C::PrimaryAttack(void)
{
	if (m_flNextPrimaryAttack > gpGlobals->curtime && m_nFireMode == FM_BURST)
	{
		return;
	}

	// Check if we should reset the shot counter based on time since last shot
	if ((gpGlobals->curtime - m_flLastAttackTime) > 0.5f)
	{
		m_nNumShotsFired = 0;
	}
	else
	{
		m_nNumShotsFired++;
	}

	m_flLastAttackTime = gpGlobals->curtime;
	m_flSoonestPrimaryAttack = gpGlobals->curtime + PISTOL_FASTEST_REFIRE_TIME;

	// Insert a combat sound
	CSoundEnt::InsertSound(SOUND_COMBAT, GetAbsOrigin(), SOUNDENT_VOLUME_PISTOL, 0.2, GetOwner());

	CBasePlayer* pOwner = ToBasePlayer(GetOwner());
	if (pOwner && m_nFireMode <= FM_SINGLE)
	{
		// Reset view punch to prevent aim drift
		pOwner->ViewPunchReset();
	}

	switch (m_nFireMode)
	{
	case FM_SINGLE:
	case FM_FULLAUTO:
		BaseClass::PrimaryAttack();
		m_flNextPrimaryAttack = gpGlobals->curtime + GetFireRate();
		break;
	case FM_BURST:
		if (m_flNextPrimaryAttack < gpGlobals->curtime)
		{
			if (burstFire < 3)
			{
				BaseClass::PrimaryAttack();
				burstFire++;
				m_flNextPrimaryAttack = gpGlobals->curtime + GetFireRate();
			}
			else
			{
				m_flNextPrimaryAttack = gpGlobals->curtime + 0.3f;
				burstFire = 0;
			}
			
		}
		break;
	default:
		DevMsg("Unknown fire mode: %d\n", m_nFireMode.Get());
		break;
	}

	// Apply an accuracy penalty
	m_flAccuracyPenalty += PISTOL_ACCURACY_SHOT_PENALTY_TIME;

	// Track weapon usage stats
	m_iPrimaryAttacks++;
	gamestats->Event_WeaponFired(pOwner, true, GetClassname());
}

